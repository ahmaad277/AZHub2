export function LanguageProvider({ children, defaultLanguage = "en" }: LanguageProviderProps) {
  const [language, setLanguage] = useState<Language>(
    () => (localStorage.getItem("language") as Language) || defaultLanguage
  );

  useEffect(() => {
    syncDocumentLanguage(language);
  }, [language]);

  const t = (key: string, langOrParams?: Language | TParams, maybeLang?: Language): string => {
    let targetLang: Language = language;
    let params: TParams | undefined;

    if (langOrParams === "en" || langOrParams === "ar") {
      targetLang = langOrParams;
    } else if (
      langOrParams !== undefined &&
      typeof langOrParams === "object" &&
      !Array.isArray(langOrParams)
    ) {
      params = langOrParams;
      if (maybeLang === "en" || maybeLang === "ar") {
        targetLang = maybeLang;
      }
    }

    const localeTable = translations[targetLang] as Record<string, string>;
    let s = localeTable[key] ?? key;
    if (params) {
      for (const [k, v] of Object.entries(params)) {
        s = s.replaceAll(`{{${k}}}`, String(v));
      }
    }
    return s;
  };

  return (
    <LanguageProviderContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageProviderContext.Provider>
  );
}

