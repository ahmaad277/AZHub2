import { createContext, useContext, useState, useEffect } from "react";
import { syncDocumentLanguage } from "@/lib/sync-document-language";

type Language = "en" | "ar";

type TParams = Record<string, string | number>;

type LanguageProviderProps = {
  children: React.ReactNode;
  defaultLanguage?: Language;
};

type LanguageProviderState = {
  language: Language;
  setLanguage: (language: Language) => void;
  t: (key: string, langOrParams?: Language | TParams, maybeLang?: Language) => string;
};

const LanguageProviderContext = createContext<LanguageProviderState | undefined>(undefined);

// Custom hook - must be defined before being exported to fix Fast Refresh
export function useLanguage() {
  const context = useContext(LanguageProviderContext);
  if (context === undefined) {
    throw new Error("useLanguage must be used within a LanguageProvider");
  }
  return context;
}

